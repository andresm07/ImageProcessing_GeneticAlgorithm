    private void btnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarActionPerformed
        // TODO add your handling code here:
        if(jLabel1.getIcon() == null)
        {
            JOptionPane.showMessageDialog(null,"Seleccione una imagen");
            return;
        }
        
        InterfazEjecucion ie = new InterfazEjecucion();
        ie.setImage(this.imagenMeta);
        ie.setTamano(this.width,this.height);
        ie.cargarImagen(this.imagenMeta);
        int poblacion = (int) spPoblacion.getValue();
        int cruces = (int) spCruce.getValue();
        int genes = (int) spGenes.getValue();
        int individuos = (int) spIndividuos.getValue();
        if((poblacion != 0) && (cruces != 0) && (genes != 0) && (individuos != 0))
        {
            ie.getParametros(poblacion, cruces, genes, individuos);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Ingrese todos los datos");
        }
        if((!rBtnSimilitud.isSelected()) && (!rBtnManhattan.isSelected()) && (!rBtnPropio.isSelected()))
        {
            JOptionPane.showMessageDialog(null,"Seleccione un algoritmo de distancia");
        }
        else
        {
            ie.setVisible(true);
            this.toBack();
        }
        

        
    }//GEN-LAST:event_btnEjecutarActionPerformed

