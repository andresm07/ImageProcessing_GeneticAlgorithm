    private void btnCargarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarImagenActionPerformed
        // TODO add your handling code here:
        JFileChooser fileExplorer = new JFileChooser("D:\\Andr√©s Miranda Arias\\Documents\\Analisis de Algoritmos\\Tarea Programada 1\\tareaprogramada1");
        int returnValue = fileExplorer.showOpenDialog(null);
        if(returnValue == JFileChooser.APPROVE_OPTION)
        {
            File selectedFile = fileExplorer.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            BufferedImage img = null;
            try {
                img = ImageIO.read(new File(path));
            } catch (IOException ex) {
                Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.imagenMeta = img;
            this.width = img.getWidth();
            this.height = img.getHeight();
            dimg = img.getScaledInstance(jLabel1.getWidth(), jLabel1.getHeight(), Image.SCALE_SMOOTH);
            jLabel1.setIcon(new ImageIcon(dimg));
            //System.out.println(selectedFile.getName());
        }
        //System.out.println("width"+this.width+" height"+this.height);
    }//GEN-LAST:event_btnCargarImagenActionPerformed

