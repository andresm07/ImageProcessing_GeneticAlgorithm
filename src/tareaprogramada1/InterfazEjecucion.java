    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        int r;
        BufferedImage img;
        Image simg;
        for(int i = 0; i < this.imagenes.length; i++)
        {
            r = (int) (Math.random() * this.p.population.size());
            img = this.p.population.get(r).individuo;
            simg = img.getScaledInstance(this.imagenes[i].getWidth(), this.imagenes[i].getHeight(), Image.SCALE_SMOOTH);
            this.imagenes[i].setIcon(new ImageIcon(simg));
        }
        
        for(int i = 0; i < this.p.population.size(); i++)
        {
            try
            {
                String name = "output" + Integer.toString(i) + ".png";
                File f = new File ("D:\\Andrés Miranda Arias\\Documents\\Analisis de Algoritmos\\Tarea Programada 1\\tareaprogramada1\\outputimages\\" + name);
                ImageIO.write(this.p.population.get(i).individuo,"png",f);
            }
            catch(IOException e)
            {
                System.out.println(e);
            }
        }
        
        this.p.setPorcentajeMutacion(this.genes);
        this.p.mutarPoblacion();
        this.p.calcularFitness();
        this.p.setProbabilidadCruce(this.cruce);
        this.p.crossOver();
    }//GEN-LAST:event_btnGenerarActionPerformed
    
