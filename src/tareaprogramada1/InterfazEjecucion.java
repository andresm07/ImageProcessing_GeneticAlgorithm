    public void getParametros(int poblacion, int cruce, int genes, int individuos)
    {
        this.poblacion = poblacion;
        this.cruce = cruce;
        this.genes = genes;
        this.individuos = individuos;
        this.p = new Poblacion(this.imagenMeta,this.poblacion);
        for(int i = 0; i < this.p.population.size(); i++)
        {
            try
            {
                String name = "output" + Integer.toString(i) + ".png";
                File f = new File ("D:\\Andrés Miranda Arias\\Documents\\Analisis de Algoritmos\\Tarea Programada 1\\tareaprogramada1\\outputimages\\" + name);
                ImageIO.write(this.p.population.get(i).individuo,"png",f);
            }
            catch(IOException e)
            {
                System.out.println(e);
            }
        }
    }
    
